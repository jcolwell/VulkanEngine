#include "Application.h"
#include <stdexcept>
#include <iostream>
//======================================================================================

int main()
{
	Application app;
	
	try
	{
		app.Initialize();
		app.Run();
		app.Terminate();
	}
	catch (std::runtime_error& e)
	{
		std::cerr << e.what() << std::endl;
		return EXIT_FAILURE;
	}
	
	return EXIT_SUCCESS;
}
